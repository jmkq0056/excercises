package session1;

/**
 * This class demonstrates the use of variables and basic arithmetic operations.
 */
public class B_Variables {

    public static void main(String[] args) {

        // Initializing value1 with 1024
        int value1 = 1024;

        // Displaying the value of value1
        System.out.println("Value 1 contains: " + value1);

        // Dividing value1 by 2 and storing the result in value2
        int value2 = value1 / 2;

        // Displaying the calculation and result stored in value2
        System.out.println("Value 2 contains value1 / 2: " + value2);
    }
}
